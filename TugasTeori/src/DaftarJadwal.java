    public void bersih(){
        txtIDJadwal.setText("");
        txtnamaProker.setSelectedIndex(-1);
        cmbDepar.setSelectedIndex(0);
        txttanggal.setText("");
        txtwaktu.setText("");
        txttempat.setText("");
    }
    private void TabelJadwalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelJadwalMouseClicked
        int i = TabelJadwal.getSelectedRow();
        DefaultTableModel dtm = (DefaultTableModel) TabelJadwal.getModel();
        txtIDJadwal.setText(dtm.getValueAt(i, 0).toString());
        txtnamaProker.setSelectedItem(dtm.getValueAt(i, 2).toString());
        cmbDepar.setSelectedItem(dtm.getValueAt(i, 1).toString());
        txttanggal.setText(dtm.getValueAt(i, 3).toString());
        txtwaktu.setText(dtm.getValueAt(i, 4).toString());
        txttempat.setText(dtm.getValueAt(i, 5).toString());

    }//GEN-LAST:event_TabelJadwalMouseClicked

